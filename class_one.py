# Look up OOP definitions and concepts
# Object-Oriented Programming (OOP) is a programming paradigm based on the concept of "objects", which can contain data and code: data in the form of fields (often known as attributes or properties), and code, in the form of procedures (often known as methods).
# Key OOP concepts include:
# 1. Classes and Objects
# 2. Inheritance
# 3. Polymorphism
# 4. Encapsulation

# key terminologies and meanings in OOP
# 1. Class: A blueprint for creating objects (a particular data structure), defining initial values (attributes or properties) and behaviors (methods or functions).
# 2. Object: An instance of a class. It is created from a class and can have its own unique attributes and behaviors.
# 3. Inheritance: A mechanism where a new class can inherit attributes and methods from an existing class, promoting code reusability.
# 4. Polymorphism: The ability to present the same interface for different underlying data types. It allows methods to do different things based on the object it is acting upon.
# 5. Encapsulation: The bundling of data (attributes) and methods (functions) that operate on the data into a single unit or class, restricting direct access to some of the object's components.